// ============================================================================
// Модуль: converter_ram 
// Назначение: двухпортовое синхронное ОЗУ (32 слова × 32 бита)
// - Запись: по переднему фронту clk при wr_en = 1
// - Чтение: асинхронное (rdata обновляется сразу при изменении addr)
// - Сброс: асинхронный (активный низкий), обнуляет все 32 ячейки памяти
// Используется для хранения промежуточных данных при потоковых операциях
// ============================================================================
module converter_ram (
    input  logic        clk,      // тактовый сигнал (запись синхронная)
    input  logic        rst_n,    // асинхронный сброс (0 – обнулить память)
    input  logic [4:0]  addr,     // адрес ячейки (5 бит → 32 слова)
    input  logic [31:0] wdata,    // данные для записи
    input  logic        wr_en,    // разрешение записи (1 – записать, 0 – читать)
    output logic [31:0] rdata     // прочитанные данные (асинхронные)
);

    // Память: 32 строки по 32 бита
    logic [31:0] mem [0:31];

    // ------------------------------------------------------------
    // Синхронная запись + асинхронный сброс
    // ------------------------------------------------------------
    always_ff @(posedge clk or negedge rst_n) begin
        if (!rst_n) begin
            // Сброс: зануляем все ячейки памяти 
            for (int i = 0; i < 32; i++) 
                mem[i] <= 32'h0;
        end else if (wr_en) begin
            // Запись: сохраняем данные по указанному адресу
            mem[addr] <= wdata;
        end
    end

    // ------------------------------------------------------------
    // Асинхронное чтение: значение на шине rdata меняется сразу
    // при изменении addr (без ожидания такта)
    // ------------------------------------------------------------
    assign rdata = mem[addr];

endmodule