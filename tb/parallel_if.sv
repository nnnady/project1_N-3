// ============================================================================
// Интерфейс внешней параллельной шины (для тестового окружения)
// ============================================================================
interface parallel_if;
    logic        clk;            // Системный тактовый сигнал
    logic        ext_ready;      // Ведомый готов
    logic        ext_ack;        // Подтверждение
    logic [15:0] ext_rdata;      // Данные от ведомого
    logic [15:0] ext_addr;       // Адрес
    logic [1:0]  ext_cmd;        // Команда
    logic [15:0] ext_wdata;      // Данные для записи ведомому
    logic        ext_done;       // Транзакция завершена
    logic        ext_word_done;  // Слово завершено
    logic        ext_stream;     // Признак потока
    logic        ext_tick;       // Строб частоты (делитель)
    logic        ext_half;       // 0 – младшая половина, 1 – старшая
endinterface
